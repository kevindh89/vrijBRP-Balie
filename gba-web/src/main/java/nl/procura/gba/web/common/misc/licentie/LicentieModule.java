package nl.procura.gba.web.common.misc.licentie;

public enum LicentieModule {

  Verhuizingen("bvh", "Verhuizingen"),
  Reisdocumenten("reisd", "Reisdocumenten"),
  Rijbewijzen("rijb", "Rijbewijzen");

  private String descr = "";
  private String type  = "";

  LicentieModule(String type, String descr) {

    setType(type);
    setDescr(descr);
  }

  public String getDescr() {
    return descr;
  }

  public void setDescr(String descr) {
    this.descr = descr;
  }

  public String getType() {
    return type;
  }

  public void setType(String type) {
    this.type = type;
  }

  @Override
  public String toString() {
    return getDescr();
  }
}
